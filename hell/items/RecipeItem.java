package items;

import interfaces.Hero;
import interfaces.Recipe;

import java.util.List;

public class RecipeItem implements Recipe {

    private String name;
    private int strengthBonus;
    private int agilityBonus;
    private int intelligenceBonus;
    private int hitPointsBonus;
    private int damageBonus;
    private List<String> requiredItems;


    public RecipeItem(String name, int strengthBonus, int agilityBonus, int intelligenceBonus,
                      int hitPointsBonus, int damageBonus, List<String> requiredItems) {
        this.name = name;
        this.strengthBonus = strengthBonus;
        this.agilityBonus = agilityBonus;
        this.intelligenceBonus = intelligenceBonus;
        this.hitPointsBonus = hitPointsBonus;
        this.damageBonus = damageBonus;
        this.requiredItems = requiredItems;
    }

    @Override
    public String getName() {
        return this.name;
    }

    @Override
    public int getStrengthBonus() {
        return this.strengthBonus;
    }

    @Override
    public int getAgilityBonus() {
        return this.agilityBonus;
    }

    @Override
    public int getIntelligenceBonus() {
        return this.intelligenceBonus;
    }

    @Override
    public int getHitPointsBonus() {
        return this.hitPointsBonus;
    }

    @Override
    public int getDamageBonus() {
        return this.damageBonus;
    }

    @Override
    public List<String> getRequiredItems() {
        return this.requiredItems;
    }
}
